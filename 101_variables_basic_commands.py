# Variables in Python
    # A variable is a box. You can give it a name and out stuff inside

# Assigning a variable
# Giving the box a name and putting stuff inside
box_variable = 'Books and Stuff'

# Looking inside the box
print(box_variable)

#Re assigning a varible
# Variables are mutables, hence they can changed / be re-assigned

box_variable = "Cd's and other stuff"
print(box_variable)

#Re-assigning with an Integer
# A variable can hold any type of data type inside
box_variable = 14
print(box_variable)

# Important python function
# print(arg)

print('Hello') # Printing string
print(box_variable) # Printing Variable
print('Hello', box_variable, 5*5, 'etc') #Overloading with two arguments

