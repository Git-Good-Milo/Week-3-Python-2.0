# Casting is When you change an object datatype to a specific data type

# String to Integer
my_var = '10'
print(type(my_var))


my_casted_var = int(my_var)
print('my_casted_var:',type(my_casted_var))

# integer/floats to string

my_int_var = 14
my_casted_str = str(my_int_var)

print('my_casted_str:', type(my_casted_str))